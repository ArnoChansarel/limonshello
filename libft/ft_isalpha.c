#include "../includes/minishell.h"

int	ft_isalpha(int c)
{
	if ((c < 'A') || (c > 'Z' && c < 'a') || (c > 'z'))
		return (0);
	return (1);
}

int	ft_isalpha_(int c)
{
	if (((c >= 'A') && (c <= 'Z')) || (((c >= 'a') && (c <= 'z')))
		|| (c == '_'))
		return (0);
	return (1);
}
